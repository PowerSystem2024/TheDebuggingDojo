#Repaso Diccionarios
diccionarioNuevo = {
    "Azul": "Blue", 
    "Rojo": "Red", 
    "Verde": "Green", 
    "Amarillo": "Yellow"
    }
print(diccionarioNuevo)

#eliminar un elemento del diccionario
del (diccionarioNuevo["Azul"])
print(diccionarioNuevo)

# Podemos almacenar diferentes tipos de datos
diccionario2 = {
    "Paula" : {
        "Edad" : 23,
        "Altura" : 1.60
    },
    "Florencia" : [23, 1.60]
}
print(diccionario2)